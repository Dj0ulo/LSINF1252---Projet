#ifndef FILES_H_INCLUDED
#define FILES_H_INCLUDED

/**
 * Imprime deux chaînes de caractères séparées par " : "
 * @param const char * : première chaîne de caractères
 * @param const char * : seconde chaîne de caractères
 * @return void
 */
void logi(const char *,const char *);

/**
 * Imprime une chaîne de caractères et un entier séparées par " : "
 * @param const char * : première chaîne de caractères
 * @param int : entier à imprimer
 * @return void
 */
void logd(const char *,const int);

#endif // FILES_H_INCLUDED
